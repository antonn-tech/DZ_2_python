# --- ЗАВДАННЯ 1: Квадрат числа ---
# Коментар: Програма приймає число та виводить його квадрат.
number_1 = float(input("1. Введіть число для піднесення до квадрата: "))
print(f"Квадрат числа: {number_1 ** 2}\n")

# --- ЗАВДАННЯ 2: Середнє трьох чисел ---
# Коментар: Просимо ввести три числа та рахуємо середнє арифметичне.
print("2. Введіть три числа:")
n1 = float(input("   Перше число: "))
n2 = float(input("   Друге число: "))
n3 = float(input("   Третє число: "))
average = (n1 + n2 + n3) / 3
print(f"Середнє: {average}\n")

# --- ЗАВДАННЯ 3: Перетворення хвилин у години ---
# Коментар: Використовуємо цілочисельне ділення та залишок.
total_min = int(input("3. Введіть кількість хвилин: "))
hrs = total_min // 60
mins = total_min % 60
print(f"{hrs} години {mins} хвилин\n")

# --- ЗАВДАННЯ 4: Розрахунок знижки ---
# Коментар: Розраховуємо ціну з урахуванням відсотка знижки.
price = float(input("4. Введіть ціну товару: "))
discount = float(input("   Введіть знижку (%): "))
final_price = price - (price * discount / 100)
print(f"Ціна зі знижкою: {final_price}\n")

# --- ЗАВДАННЯ 5: Остання цифра числа ---
# Коментар: Остача від ділення на 10 дає останню цифру.
num_last = int(input("5. Введіть ціле число: "))
print(f"Остання цифра: {num_last % 10}\n")

# --- ЗАВДАННЯ 6: Периметр прямокутника ---
# Коментар: Периметр = 2 * (сума довжини і ширини).
length = float(input("6. Введіть довжину прямокутника: "))
width = float(input("   Введіть ширину прямокутника: "))
perimeter = 2 * (length + width)
print(f"Периметр: {perimeter}\n")

# --- ЗАВДАННЯ 7: Виведення 4-х значного числа в стовпчик ---
# Коментар: Використовуємо математичні методи (// та %) для отримання розрядів.
full_num = int(input("7. Введіть 4-х значне число: "))
d1 = full_num // 1000          # Тисячі
d2 = (full_num // 100) % 10    # Сотні
d3 = (full_num // 10) % 10     # Десятки
d4 = full_num % 10             # Одиниці

print(d1)
print(d2)
print(d3)
print(d4)
